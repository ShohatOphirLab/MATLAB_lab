classdef PaletteType
    % PaletteType - Enumeration class for representing different color palettes
    % in the ScriptChooserApp.
    %
    % This enumeration lists the available color palettes that users can 
    % select from the PaletteDropDown menu, and these palettes are used
    % in the visual output of the selected analysis.
    
    enumeration
        Happy   % Happy color palette
        Rainbow % Rainbow color palette
    end
end
